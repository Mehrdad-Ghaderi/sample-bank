package com.mehrdad.sample.bank.core.mapper;

import com.mehrdad.sample.bank.api.dto.account.AccountDto;
import com.mehrdad.sample.bank.core.entity.AccountEntity;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;

import java.util.List;

/**
 * Created by Mehrdad Ghaderi
 */

@Mapper(componentModel = "spring")
public interface AccountMapper {

    // DTO -> Entity
    @Mapping(target = "id", ignore = true) // always generated by JPA
    @Mapping(target = "customer", ignore = true) // will set manually
    AccountEntity toAccountEntity(AccountDto dto);

    List<AccountEntity> toAccountEntityList(List<AccountDto> dtos);

    // Entity -> DTO
    AccountDto toAccountDto(AccountEntity entity);

    List<AccountDto> toAccountDtoList(List<AccountEntity> entities);
}